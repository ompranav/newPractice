package com.amazon.alexa.demo;

public class Encapsulate {
	private String name;
	private int age;
	private int roll;
	
	public String getName() {
		return name;
	}
	public int getAge() {
		return age;
	}
	public int getRoll() {
		return roll;
	}
	
	public void setName(String newName) {
		name = newName;
		}
	public void setAge(int newAge) {
		age = newAge;
	}
	public void setRoll(int newRoll) {
		roll = newRoll;
	}

}
